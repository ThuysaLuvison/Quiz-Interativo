let indicePergunta = 0;
let pontuacao = 0;
let tempoRestante = 15;
let timer;

const perguntaE1 = document.getElementById("pergunta");
const opcoesE1 = document.getElementById("opcoes");
const proximoBtn = document.getElementById("proximo-btn");
const resultadoE1 = document.getElementById("resultado");
const timerE1 = document.getElementById("timer");

function iniciarTimer() {
    tempoRestante = 15;
    timerE1.textContent = `‚è≥ ${tempoRestante}s`;

    timer = setInterval(() => {
        tempoRestante--;
        timerE1.textContent = `‚è≥ ${tempoRestante}s`;
        if (tempoRestante <=0){
            clearInterval(timer);
            bloquearRespostas();
            proximoBtn.style.display = "block";
        }
    }, 1000);
}

function carregarPergunta(){
    clearInterval(timer);
    iniciarTimer();
    proximoBtn.style.display = "none";
    resultadoE1.textContent = "";
    const perguntaAtual = perguntas[indicePergunta];
    perguntaE1.textContent = perguntaAtual.pergunta;
    opcoesE1.innerHTML = "";

    perguntaAtual.opcoes.forEach((opcao, index) => {
    const botao = document.createElement("button");
    botao.textContent = opcao;
    botao.classList.add("btn");
    botao.addEventListener("click", () => verificarResposta(index));
    opcoesE1.appendChild(botao);
    });
}

function verificarResposta(indice){
    clearInterval(timer);
    const perguntaAtual = perguntas[indicePergunta];
    const botoes = document.querySelectorAll("#opcoes button");
    botoes.forEach((btn, i) => {
        if (i === perguntaAtual.resposta) {
            btn.classList.add("resposta-certa");
        } else if (i === indice) {
            btn.classList.add("resposta-errada");
        }
        btn.disabled = true;
    });

    if (indice === perguntaAtual.resposta) {
        let bonus = tempoRestante;
        pontuacao+= 10 + bonus;
        resultadoE1.textContent = `‚úÖ Acertou! +${10 + bonus} pontos`;
    } else {
        resultadoE1.textContent = `‚ùå Errou!`;
    }
    proximoBtn.style.display = "block";
}

    function bloquearRespostas() {
const botoes = document.querySelectorAll("#opcoes button");
botoes.forEach(btn => btn.disabled = true);
    }

proximoBtn.addEventListener("click", () => {
    indicePergunta++;
    if (indicePergunta < perguntas.length) {
        carregarPergunta();
    } else {
        mostrarResultado();
    }
});

function mostrarResultado() {
    perguntaE1.textContent = "üèÜ Quiz Finalizado!";
    opcoesE1.innerHTML = "";
    resultadoE1.textContent = `Voc√™ fez ${pontuacao} pontos! üéâ`;
    proximoBtn.style.display = "none";
    timerE1.style.display = "none";
}

